/**
 * Hotel Booking Application Entry Point
 *
 * This class represents the starting point of the Hotel Booking System.
 * It demonstrates how a Java application begins execution and displays
 * a welcome message to the user.
 *
 * The program prints the application name and version information
 * to the console and then terminates.
 *
 * @author Aditi
 * @version 1.0
 */
public class HotelBookingApp {

    /**
     * Main method - Entry point of the application.
     * JVM starts execution from here.
     *
     * @param args Command line arguments (not used here)
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("=================================");
        System.out.println("   Welcome to Hotel Booking App  ");
        System.out.println("=================================");

        // Application details
        System.out.println("Application Name: Hotel Booking System");
        System.out.println("Version: v1.0");

        // End message
        System.out.println("\nApplication started successfully!");
    }
}